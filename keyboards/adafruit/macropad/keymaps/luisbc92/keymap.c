// Layer Set Callback
layer_state_t layer_state_set_user(layer_state_t state) {

    // Swish Super
    if (layer_state_cmp(state, LSWISH)) {
        register_code(SUPER);
    } else {
        unregister_code(SUPER);
    }

    return state;
}

// Key Processing
bool process_record_user(uint16_t keycode, keyrecord_t *record) {

    // Snapping on Tap, Screens on Hold
    switch (keycode) {
        case LT(0, LEFT):
            if (record->tap.count && record->event.pressed) {
                tap_code16(S(LEFT));
            } else if (record->event.pressed) {
                tap_code16(G(LEFT));
            }
            return false;

        case LT(0, RIGHT):
            if (record->tap.count && record->event.pressed) {
                tap_code16(S(RIGHT));
            } else if (record->event.pressed) {
                tap_code16(G(RIGHT));
            }
            return false;

        case LT(0, UP):
            if (record->tap.count && record->event.pressed) {
                tap_code16(S(UP));
            } else if (record->event.pressed) {
                tap_code16(G(UP));
            }
            return false;

        case LT(0, DOWN):
            if (record->tap.count && record->event.pressed) {
                tap_code16(S(DOWN));
            } else if (record->event.pressed) {
                tap_code16(G(DOWN));
            }
            return false;
    }

    return true;
}


